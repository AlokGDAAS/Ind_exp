def sum(a,b):
    return a+b

def subtraction(a,b):
    return a-b

def multiply(a,b):
    return a*b

def devide(a,b):
    return a/b

num1 =int(input("Enter your first number: "))
num2 =int(input("Enter your second number: "))

print("Sum : ",sum(num1,num2))
print("Subraction : ",subtraction(num1,num2))
print("Multiply : ",multiply(num1,num2))
print("Devide : ",devide(num1,num2))